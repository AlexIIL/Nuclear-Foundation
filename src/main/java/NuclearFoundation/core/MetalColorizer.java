package NuclearFoundation.core;

public class MetalColorizer {
	
	public static int getColorForMetal(String metal){
		switch(metal){
		case("Boron"):return 0x0;
		case("Aluminium"):return 0x0;
		case("Titanium"):return 0x0;
		case("Cromium"):return 0x0;
		case("Iron"):return 0x0;
		case("Cobalt"):return 0x0;
		case("Nickel"):return 0x0;
		case("Copper"):return 0x0;
		case("Zinc"):return 0x0;
		case("Zirconium"):return 0x0;
		case("Silver"):return 0x0;
		case("Tin"):return 0x0;
		case("Wolfram"):return 0x0;
		case("Osmium"):return 0x0;
		case("Platinum"):return 0x0;
		case("Gold"):return 0x0;
		case("Lead"):return 0x0;
		case("Bismuth"):return 0x0;
		case("Thorium"):return 0x0;
		case("Uranium"):return 0x0;
		case("Mithril"):return 0x0;
		case("Adamantine"):return 0x0;
		default:return -1;
		}
	}
}
